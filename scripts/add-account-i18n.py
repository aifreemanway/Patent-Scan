def main():
    root = Path(__file__).resolve().parent.parent
    for filename, block in (("ru.json", ACCOUNT_RU), ("en.json", ACCOUNT_EN)):
        path = root / "messages" / filename
        data = json.loads(path.read_text(encoding="utf-8"))
        data["Account"] = block
        path.write_text(
            json.dumps(data, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
            newline="\n",
        )
        print(f"Updated {path}: {len(data)} namespaces, Account has {len(block)} sub-namespaces")


if __name__ == "__main__":
    main()
